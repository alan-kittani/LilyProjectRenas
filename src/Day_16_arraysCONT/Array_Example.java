package Day_16_arraysCONT;

public class Array_Example {

    public static void main(String[] args) {


        /**
         *    Swapping elements in an array
         */

        int [] swapArray = {30, 90};

        int holdingVariable = swapArray[0]; // we store our index 0 value (30) into this int
        swapArray[0] = swapArray[1];
        swapArray[1] = holdingVariable;

        // swapArray = {90, 30};





























    }
}
