package Day_15_ArrayIntro;

import java.util.Arrays;

public class Topic1_Array {

    public static void main(String[] args) {


        /**
         *    Arrays are containers that contain multiple variables
         *    Instead of :
         *    int a = 1
         *    int b = 2
         *    int c = 3
         *
         *    We can do :
         *    data type [] name = {myValue1, myValue2, myValue3, .... } - this is one way of defining the Array.
         *
         *    ARRAYS ARE INDEXED, and they also start from 0 just like strings
         */

        int [] hi = {1,       2,       3,       4,       5,       6};
        // index     0        1        2        3        4        5

        int int1 = 1, int2 = 2, int3 = 3;

        String a = "Whatever";
        String b = "However";

        //  array Strings
        String [] c = {"Whatever" , "However"};

        char char1 = 'r';
        char char2 = 't';
        char char3 = 'y';

        // array Char

        char [] CHAR = {'r' , 't' , 'y'};

        // array Boolean

        boolean bool = true;
        boolean bool2 = false;
        boolean bool3 = true;

        boolean [] BOOL = {true , false , true};

        // Object [] obj = {Object1, Object2, Object3}   (arrays contain non-primitive data types as well, such as strings)




    }
}
