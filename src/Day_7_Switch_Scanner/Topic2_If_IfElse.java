package Day_7_Switch_Scanner;

public class Topic2_If_IfElse {

    public static void main(String[] args) {

        String cls = "HTML";
        if (cls != "JAVA") {
            System.out.println("Person is added to Java class");
        }
        if (cls != "HTML") {
            System.out.println("Person is added to HTML class");
        }
        if (cls != "CSS") {
            System.out.println("Person is added to HTML class");
        }







    }
}
