package Day_10_Strings_Continued;

public class Topic_3_ForLoop {

    public static void main(String[] args) {

        /** Loops will help you on continuous actions.
         *
         * You will always need to specify these in the loop:
         * Starting point,
         * Ending point,
         * Iteration,
         */


        /**
         * for (starting ; endpoint ; iteration) {
         *
         * }
         * an example use for a for loop is driving hourly mileage.
         */

        for (int i = 0; i < 20; i++) {
         // starting    ending   iteration

            System.out.println("Increase: " +i);
        }
        for (int j = 18; j >= 0; j --) {
            System.out.println("Decrease: " +j);
        }






    }
}
