package Day_2variables;

public class Variables {

    public static void main(String[] args) {
        // boolean, It is true or false. It is 1 bit,

        boolean bolvalue = true;
        System.out.println(bolvalue);
        bolvalue = false;
        System.out.println(bolvalue);

        // byte 8 bit, value between -128 to 127

        byte bytevalue = 120;
        System.out.println(bytevalue);



        // char (character) 16 bits, it contains characters , numbers and so on

        char charvalue = 'W';
        System.out.println(charvalue);

        // short 16 bits takes values -32768 to 32767

        short shortvalue = 20000;
        System.out.println(shortvalue);

        // int (integers) 32 bits, -2 bil and 2 bil

        int intvalue = 400000000;
        System.out.println(intvalue);

        // long 64 bits memory, goes up to trillions and lowest to trillions

        long longvalue = 2000000000000000000l;
        System.out.println(longvalue);

        // float 32 bits and holds up to 5 decimal digits

        float floatvalue = 123.7f;
        System.out.println(floatvalue);

        // double 64 bits and up to 16 decimal digits

        double doublevalue = 123.8428492482323232323;
        System.out.println(doublevalue);

        // non-primitive,

    }
}
